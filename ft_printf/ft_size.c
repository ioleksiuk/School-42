#include "ft_printf.h"

char			*ft_size(t_pf *a, char *s)
{
	if (*s == 'h' || *s == 'l' || *s == 'j' || *s == 'z')
	{
		if (*s == 'h' && *(s + 1) != 'h' && *(s - 1) != 'h' && a->size < 2)
			a->size = 2;
		if (*s == 'h' && ((*(s + 1) == 'h' || *(s - 1) == 'h')) && a->size < 1)
		{
			a->size = 1;
			s++;
		}
		if (*s == 'l' && *(s + 1) != 'l' && *(s - 1) != 'l' && a->size < 3)
			a->size = 3;
		if (*s == 'l' && *(s + 1) == 'l' && a->size < 4)
		{
			a->size = 4;
			s++;
		}
		if (*s == 'z' && a->size < 5)
			a->size = 5;
		if (*s == 'j' && a->size < 6)
			a->size = 6;
		s++;
	}
	return (s);
}
