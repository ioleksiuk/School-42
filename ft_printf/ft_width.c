#include "ft_printf.h"

char	*ft_width(t_pf *a, char *s)
{
	if (*s > '0' && *s <= '9' && *(s - 1) != '.')
	{
		a->width = *s - '0';
		s++;
		while (*s <= '9' && *s >= '0')
		{
			a->width = a->width * 10 + *s - '0';
			s++;
		}
	}
	return (s);
}
