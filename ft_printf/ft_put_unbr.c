#include "ft_printf.h"

void			ft_put_unbr(uintmax_t n, t_pf *a)
{
	char		p;
	uintmax_t	z;

	z = n;
	if (z >= 10)
	{
		ft_putnbr(z / 10, a);
		ft_putnbr(z % 10, a);
	}
	else
	{
		p = z + 48;
		write(1, &p, 1);
		a->i++;
	}
}
