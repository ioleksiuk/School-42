#include "lem_in.h"

void		del_q_static(int *q, int size)
{
	int	i;

	i = 0;
	if (q[size - 1] != -1)
	{
		q[size - 1] = -1;
		return ;
	}
	while (i < size - 1)
	{
		if (q[i + 1] == -1)
		{
			q[i] = -1;
			return ;
		}
		i++;
	}
}
